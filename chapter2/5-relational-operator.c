#include<stdio.h>
int main()
{
    //relational Operators: ==, !=, >, <, >=, <=
    //used to compare two values
    int a = 5, b = 10;
    printf("a == b: %d\n", a == b); // 0 (false)
    printf("a != b: %d\n", a != b); // 1 (true)
    printf("a > b: %d\n", a > b);   // 0 (false)
    printf("a < b: %d\n", a < b);   // 1 (true)
    printf("a >= b: %d\n", a >= b); // 0 (false)
    printf("a <= b: %d\n", a <= b); // 1 (true)
    // 1 represents true and 0 represents false
    return 0;
}