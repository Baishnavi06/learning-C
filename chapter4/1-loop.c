#include<stdio.h>
int main()
{
    //i is iterator variable or counter variable
    // for loop example
    for(int i = 1; i <= 5; i++)
    {
        printf("Hello world %d\n", i);
    }
    //print numbers from 0 to 10
    for(int i = 0; i <= 10; i++)
    {
        printf("%d\n", i);
    }
    return 0;
}